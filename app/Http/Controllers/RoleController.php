     */
    public function store(Request $request)
    {
        $request->validate([
            'nama' => 'required|string|unique:service'
        ]);

        Role::create([
            'nama'      => $request->nama
        ]);

        return redirect()->route('akses.index')->with('success', 'Hak Akses Berhasil Ditambahkan !');
    }

